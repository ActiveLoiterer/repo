package vieux.foo.tap_cercle;

public class User {
	
	char sexe;
	int age;
	
	public User(char sexe, int age) {
		this.sexe = sexe;
		this.age = age;
	}

	public char getSexe() {
		return sexe;
	}

	public void setSexe(char sexe) {
		this.sexe = sexe;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
}



